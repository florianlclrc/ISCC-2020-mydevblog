    <img src="marketing.jpg" width="500"> <h2>Le marketing C to C</h2>

    <h4>
    Mais qu’est-ce que le marketing C to C ? Pour ceux ayants des notions d’anglais, le terme de marketing consumer to consumer les aidera à comprendre, pour les autres, il s’agit du marketing d’un individu à un autre.

Moins connu que les marketing B to B (entreprise à entreprise), ou B to C (entreprise à consommateur), il n’en est pas moins fréquent. Cette technique représente donc la vente de biens ou services dans un cadre non professionnel. 

Les exemples les plus flagrants pour cette technique sont les sites tel Vinted ou Leboncoin. Leur développement est tel qu’à présent il existe des sites spécialisés pour presque chaque marchés. Cela va de la seconde main d’articles de luxe avec Vestiaire Collective, ou Troc.com pour le bricolage par exemple.

</h4>

    <h3>Un marketing très consommé</h3>

    <h4>
    En France, on estime qu’une personne sur deux est adepte de la revente, selon une étude réalisée par TNS Sofres. C’est donc une tendance de fond modifiant les comportements des français, motivés par des raisons la plupart du temps budgétaires ou écologiques. 

Si ces tendances sont autant plébiscitées, c’est grâce à l’essor d’Internet. On estime que 65% des urbains sont intéressés soit par l’achat, soit par la vente d’habits déjà portés.  Il est vrai que cette pratique rend le shopping beaucoup plus accessible, et peux permettre de retrouver des pièces de collections non disponibles en magasin ! 

  Les deux leaders en France du marketing C to C sont Leboncoin et eBay, cela vous étonne ? N’avez-vous-même jamais commandé sur ces sites ? Leur simplicité d’utilisation et l’énorme offre de gammes disponibles font de ces sites les deux plus utilisés en France, avec un chiffre d’affaire de 3 milliards de dollar pour eBay, et de 257 millions d’euro pour Leboncoin en 2019.
</h4>

    <h3>Que des avantages à la seconde main ?</h3>

    <h4>
    Bien que simples d’utilisation, quelques freins sont quand même facilement identifiables sur ces sites : 

Le premier élément est le manque de garantie. En effet, sur chacun des sites précédemment cités, vous ne pouvez pas totalement être protéger d’une arnaque sur les produits, sans garantie d’être remboursé ou autre en cas de problèmes, les échanges concernant des particuliers et non la plateforme. 

Le second frein concerne le service après-vente, qui comme la garantie varie selon les sites d’achat. Si l’on se trompe dans la taille d’un t-shirt par exemple, il vous sera impossible de le renvoyer pour l’échanger, étant donné que c’est une pièce unique. L’erreur n’est donc pas protégée

Le troisième frein de la seconde main est l’absence de sécurité lors des transactions financières. Réel problème lors de la décision d’achat, les payements s’effectuent généralement en virements et non pas sur l’application, ce qui inclus une relation de confiance entre deux inconnus. 

Un quatrième et dernier problème serait logistique, avec des difficultés d’utilisation des sites, comme des bugs, ou des photos trompeuses non contrôlées par les responsables.

    </h4>

    <h3>En route vers des jours meilleurs</h3>

    <h4>Bien que les freins soient multiples, ils sont relativement faciles à résoudre. C’est notamment ce qu’a fait Leboncoin en lançant la possibilité de payer son article par leur site. De réelles opportunités s’offrent à ce marketing en plein développement, dans un monde où la surconsommation prône. 


    </h4>

